package org.grsstreet.model.enums;

/**
 * Enumeração que representa os tipos de produtos disponíveis no sistema.
 * Pode ser utilizada para categorizar os produtos no cadastro e na exibição.
 */
public enum TipoProduto {
    TENIS,   // Calçados esportivos
    BONE,    // Bonés e acessórios de cabeça
    CAMISA,  // Camisetas e blusas
    CALCA    // Calças e bermudas
}
